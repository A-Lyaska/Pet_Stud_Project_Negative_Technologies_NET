-- Создание функции для проверки баланса
CREATE OR REPLACE FUNCTION admin.check_balance(account_id INT) RETURNS DECIMAL AS $$
DECLARE
balance DECIMAL;
BEGIN
SELECT SUM(amount) - balance INTO balance
FROM admin.Payments, admin.Accounts
WHERE account_id = Payments.account_id AND account_id = check_balance.account_id;
RETURN balance;
END;
$$ LANGUAGE plpgsql;

-- Функция пополнения баланса
CREATE OR REPLACE FUNCTION admin.add_payment(account_id INT, amount DECIMAL, payment_date TIMESTAMP)
RETURNS VOID AS $$
BEGIN
    INSERT INTO Payments (account_id, amount, payment_date)
    VALUES (account_id, amount, payment_date);
END;
$$ LANGUAGE plpgsql;

-- Функция для обновления баланса счета
CREATE OR REPLACE FUNCTION update_account_balance()
RETURNS TRIGGER AS $$
BEGIN
    UPDATE admin.Accounts
    SET balance = balance + NEW.amount
    WHERE account_id = NEW.account_id;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Триггер, вызывающий функцию при вставке новой записи в таблицу Payments
CREATE TRIGGER after_payment_insert
AFTER INSERT ON Payments
FOR EACH ROW
EXECUTE FUNCTION update_account_balance();

