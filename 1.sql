CREATE OR REPLACE FUNCTION admin.safe_check_balance(account_id INT) RETURNS DECIMAL AS $$
DECLARE
    balance DECIMAL;
BEGIN
    SELECT SUM(amount) - balance INTO balance
    FROM admin.Payments, admin.Accounts
    WHERE Payments.account_id = account_id AND Accounts.account_id = account_id;
    RETURN balance;
END;
$$ LANGUAGE plpgsql;

